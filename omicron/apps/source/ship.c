    void createShip (int x, int y)
    {
        ship_t *s;

            if (!ship)
            {
                ship = nla (NCreate, shipD);
                if (!ship) animDie ("ship");

                sship = nla (NCreate, sshipD);
                if (!sship) animDie ("sship");

                hw = ship->xres >> 1;
                hh = ship->yres >> 1;

                hyl = sHeight - hh;
                hxl = sWidth - hw;

                lxl = hw;
                lyl = hh;
            }

            if (x == -1) return;

            s = new (ship_t);

            s->x = x;
            s->y = y;

            s->weaponCost = getWeaponCost (0);
            s->weaponEnergy = 0;
            s->t = _90deg;

            s->weapons = 1;

            s->ShipSpeed = ShipSpeed1;
            s->shield = ShieldEnergy;
            s->ships = PlayerShips;
            s->generators = 1;
            s->bombs = 0;

            s->CurMaxWeaponEnergy = MinWeaponE;

            spriteHandler (s, handler);
            addSprite (s, t__ship);

            player = s;
    }

