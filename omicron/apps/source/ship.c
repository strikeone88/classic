    static void updateAngle4 (int *t, int I, int II, int III, int IV)
    {
            switch (*t >> 6)
            {
                case 0:
                    (*t) += I;
                    break;

                case 1:
                    (*t) += II;
                    break;

                case 2:
                    (*t) += III;
                    break;

                case 3:
                    (*t) += IV;
                    break;
            }

            (*t) = ((*t) + 256) & 255;
    }

    static void updateAngle_2a (int *t, int I, int II)
    {
            if (_135deg <= *t && *t <= _315deg)
                (*t) += II;
            else
                (*t) += I;

            (*t) = ((*t) + 256) & 255;
    }

    static void updateAngle_2b (int *t, int I, int II)
    {
            if (_45deg <= *t && *t <= _225deg)
                (*t) += II;
            else
                (*t) += I;

            (*t) = ((*t) + 256) & 255;
    }

