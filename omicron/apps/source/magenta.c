    static int handler (magenta_t *p, int m, ...)
    {
        int x, y, x1, y1, w, h, b, i, t;
        static unsigned long r;
        static va_list args;
        static void *q;

            va_start (args, m);

            switch (m)
            {
                case m__damageLevel:
                    return 8;

                case m__hit:
                    if (p->r--)
                    {
                        t = va_arg (args, unsigned);

                        p->x += rcost (ShotSpeed>>1, t);
                        p->y -= rsint (ShotSpeed>>1, t);

                        return 0;
                    }
                    else
                        return 1;

                case m__suicide:
                case m__destroy:
           Destroy: createHBoom (p->x, p->y, HBoomMagenta, HBoomHDelay, 255, UNUSED, UNUSED, BoomDec);

                    if (m != m__suicide)
                        createItem (p->x, p->y, 8, 2*CLOCKS_PER_SEC, RADIAL, t__dollar, dollar, MagentaPrice);

                    removeSprite (p);
                    delete (p);

                    break;

                case m__collides:
                    if (!p->ok) break;

                    x = va_arg (args, int);
                    y = va_arg (args, int);
                    x1 = va_arg (args, int);
                    y1 = va_arg (args, int);

                    return detectCollision (p->x - hw, p->y - hh,
                            magenta->xres, magenta->yres, x, y, x1, y1);

                case m__update:
                    goto Default;
            }

            return 0;

   Default: x = p->x;
            y = p->y;

            if (x < lxl) x = lxl;
            if (y < lyl) y = lyl;
            if (x > hxl) x = hxl;
            if (y > hyl) y = hyl;

            x1 = x - hw;
            y1 = y - hh;
            w = magenta->xres;
            h = magenta->yres;

            if ((q = spriteCollision (t__ship, p, x1, y1, w, h)) != NULL)
            {
                if (!p->shield) sendMessage (q, m__destroy);
                if (sendMessage (q, m__activeShield)) goto Destroy;
            }

            if (p->shield) p->shield--;

            drawFrame (p->x = x, p->y = y, p->shield ? smagenta : magenta, p->f);

            p->ok = 1;

            if (!p->frameDelay--)
            {
                p->frameDelay = MagentaFDelay;

                p->f++;
                if (p->f >= magenta->fcount) p->f = 0;
            }

            if (!player) return 0;

            x1 = sendMessage (player, m__getX);
            y1 = sendMessage (player, m__getY);

            i = abs (x1 - x);
            m = abs (y1 - y);

            if (!i && !m) return 0;

            if (!p->p--)
            {
                if ((p->c = m > i) != 0)
                {
                    p->m = m = (x1 - x) / (y1 - y);
                    p->b = b = x - m*y;

                    if ((p->s = y1 < y) != 0)
                        p->x = m * (p->y -= MagentaSpeed) + b;
                    else
                        p->x = m * (p->y += MagentaSpeed) + b;
                }
                else
                {
                    p->m = m = (y1 - y) / (x1 - x);
                    p->b = b = y - m*x;

                    if ((p->s = x1 < x) != 0)
                        p->y = m * (p->x -= MagentaSpeed) + b;
                    else
                        p->y = m * (p->x += MagentaSpeed) + b;
                }

                p->p = MagentaUpdate;
            }
            else
            {
                if (p->c)
                {
                    if (p->s)
                        p->x = p->m * (p->y -= MagentaSpeed) + p->b;
                    else
                        p->x = p->m * (p->y += MagentaSpeed) + p->b;
                }
                else
                {
                    if (p->s)
                        p->y = p->m * (p->x -= MagentaSpeed) + p->b;
                    else
                        p->y = p->m * (p->x += MagentaSpeed) + p->b;
                }
            }

            return 0;
    }

    void createMagenta (int x, int y)
    {
        static int initialized = 0;
        magenta_t *s;

            if (!initialized)
            {
                magenta = nla (NCreate, magD);
                if (!magenta) animDie ("mag");

                smagenta = nla (NCreate, smagD);
                if (!smagenta) animDie ("smag");

                hw = magenta->xres >> 1;
                hh = magenta->yres >> 1;

                hyl = sHeight - hh;
                hxl = sWidth - hw;

                lxl = hw;
                lyl = hh;

                initialized = 1;
            }

            if (x == -1) return;

            s = new (magenta_t);

            s->x = x;
            s->y = y;

            s->frameDelay = MagentaFDelay;
            s->shield = MagentaShield;

            s->r = MagentaR;

            spriteHandler (s, handler);
            addSprite (s, t__enemy);
    }

